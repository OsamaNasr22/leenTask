     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'category_title'=>'required|string',
            'category_image'=>'required|image'
        ]);
        $category= new  Category();
        $category->title=$request['category_title'];
        $category->image= $request->file('category_image')->storeAs('public\category',uniqid(rand(1,500)).'.jpg');
        return ($category->save())? redirect()->back()->with(['success'=>'Category added successfully']):
            redirect()->back()->with(['fail'=>'failed when add category,try again']);
    }

